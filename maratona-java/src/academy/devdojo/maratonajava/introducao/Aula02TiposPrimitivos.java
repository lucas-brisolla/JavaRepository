package academy.devdojo.maratonajava.introducao;

public class Aula02TiposPrimitivos {
    public static void main(String[] args) {
        int age = 10;
        long longNumber = 10000;
        short ageShort = 100;
        double salaryDouble = 2000.50;
        float salaryFloat = 2500.0f;
        byte byteNumber = 127;
        boolean yes = true;
        boolean no = false;
        char character = 65;
        String text = "adfjadkdfçlkdflçdke";

        System.out.println("The age is "+age+" years old");
        System.out.println(longNumber);
        System.out.println(ageShort);
        System.out.println(salaryDouble);
        System.out.println(salaryFloat);
        System.out.println(byteNumber);
        System.out.println(yes);
        System.out.println(no);
        System.out.println(character);
        System.out.println(text);
    }
}
